$(document).ready(function() {
    if ($(window).width() < 768) {
    $('.shop').hide();
    }
    $('.test').click(function() {
        if ($(window).width() < 768) { // Check if screen width is less than 768px
            $('.shop').toggle(); // Toggle the content visibility
        }
    });
});

